def headers_for(sheet):
    if sheet == "README":
        return ["Topic", "Details"]
    if sheet == "Architecture Principles":
        return PRINCIPLE_HEADERS
    if sheet == "Entity Index":
        return ENTITY_INDEX_HEADERS
    if sheet == "Implementation Milestones":
        return MILESTONE_HEADERS
    if sheet == "Quality Gates":
        return QUALITY_HEADERS
    if sheet == "Open Questions":
        return OPEN_HEADERS
    if sheet == "Change Log":
        return CHANGE_HEADERS
    return FIELD_HEADERS


def safe_csv_name(sheet):
    return re.sub(r"[^A-Za-z0-9]+", "_", sheet).strip("_").lower() + ".csv"


def style_sheet(ws):
    ws.freeze_panes = "A2"
    ws.auto_filter.ref = ws.dimensions
    for cell in ws[1]:
        cell.font = Font(bold=True, color="FFFFFF")
        cell.fill = PatternFill("solid", fgColor="1F2937")
        cell.alignment = Alignment(wrap_text=True, vertical="top")
    for row_cells in ws.iter_rows(min_row=2):
        for cell in row_cells:
            cell.alignment = Alignment(wrap_text=True, vertical="top")
    for idx, col in enumerate(ws.columns, 1):
        max_len = 0
        for cell in col:
            max_len = max(max_len, min(len("" if cell.value is None else str(cell.value)), 70))
        ws.column_dimensions[get_column_letter(idx)].width = max(12, min(44, max_len + 2))
    headers = [c.value for c in ws[1]]
    for header, colors in [("Source Basis", SOURCE_COLORS), ("Approval Status", APPROVAL_COLORS)]:
        if header in headers:
            col = get_column_letter(headers.index(header) + 1)
            for value, color in colors.items():
                ws.conditional_formatting.add(
                    f"{col}2:{col}{max(ws.max_row, 2)}",
                    FormulaRule(formula=[f'{col}2="{value}"'], fill=PatternFill("solid", fgColor=color)),
                )


def repo_commit():
    try:
        return subprocess.check_output(["git", "rev-parse", "HEAD"], cwd=ROOT, text=True, stderr=subprocess.DEVNULL).strip()
    except Exception:
        return ""



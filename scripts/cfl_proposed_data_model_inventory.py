def label(field):
    return re.sub(r"([a-z0-9])([A-Z])", r"\1 \2", field).replace("_", " ").replace("-", " ").title()


def row(entity, table, field, dtype, example="", parent="", layer="world", fm="", basis="domain-design",
        phase="M0+", screen="", sim="", editable="No", persist="Yes", notes=""):
    return [
        entity,
        table,
        field,
        label(field),
        dtype,
        example,
        parent,
        layer,
        fm,
        basis,
        phase,
        screen,
        sim,
        editable,
        persist,
        notes,
        "pending-review",
    ]


def rows_for(entity, table, fields, parent="", layer="world", fm="", basis="domain-design", phase="M0+",
             screen="", sim="", editable="No", persist="Yes"):
    result = []
    for item in fields:
        if len(item) == 2:
            field, dtype = item
            example = ""
            notes = ""
        elif len(item) == 3:
            field, dtype, example = item
            notes = ""
        else:
            field, dtype, example, notes = item
        result.append(row(entity, table, field, dtype, example, parent, layer, fm, basis, phase, screen, sim, editable, persist, notes))
    return result



def verify():
    wb = load_workbook(OUT_XLSX, read_only=True)
    return {
        "exists": OUT_XLSX.exists(),
        "sheet_count": len(wb.sheetnames),
        "missing_sheets": [s for s in SHEETS if s not in wb.sheetnames],
        "bad_headers": [s for s in wb.sheetnames if not wb[s].cell(1, 1).value],
        "csv_count": len(list(OUT_CSV_DIR.glob("*.csv"))),
    }



def rel(path):
    try:
        return str(Path(path).resolve().relative_to(ROOT)).replace("\\", "/")
    except Exception:
        return str(path).replace("\\", "/")


def display_label(field):
    text = re.sub(r"([a-z0-9])([A-Z])", r"\1 \2", str(field)).replace("_", " ").replace("-", " ")
    return " ".join(text.split()).title()


def infer_type(value):
    if isinstance(value, bool):
        return "boolean"
    if isinstance(value, int):
        return "integer"
    if isinstance(value, float):
        return "number"
    if isinstance(value, list):
        return "array"
    if isinstance(value, dict):
        return "object"
    if value is None:
        return "null"
    if isinstance(value, str):
        if re.match(r"^#[0-9A-Fa-f]{6}$", value):
            return "color/string"
        if re.match(r"^\d{4}-\d{2}-\d{2}", value):
            return "date/string"
        return "string"
    return type(value).__name__


def source_status(path):
    p = rel(path)
    if p.startswith(("data/", "game_data/", "data_raw/", "data_processed/", "harness/runs/")):
        return "mock-only"
    if p.startswith(("docs/", "ai-pack/", "cfl_agent_repo_pack/")):
        return "spec-only"
    if p.endswith((".md", ".txt")):
        return "spec-only"
    return "existing-code"



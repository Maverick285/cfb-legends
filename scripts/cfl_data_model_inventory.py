    summary = {
        "status": "PASS" if verification["exists"] and not verification["missing_sheets"] and not verification["no_headers"] else "FAIL",
        "workbook_path": str(OUT_XLSX),
        "csv_folder_path": str(OUT_CSV_DIR),
        "entities_found": entity_count,
        "fields_inventoried": field_count,
        "inferred_fields": inferred_count,
        "missing_or_recommended_fields": missing_count,
        "files_scanned": len(files),
        "source_folders_scanned": sorted({rel(f).split("/")[0] for f in files}),
        "source_folders_skipped": [".git", ".venv", "node_modules", "artifacts", "tmp", "data_model_inventory_csv generated exports", "harness/runs generated outputs", "docs/archive", "Screens for game", "*.svg/images"],
        "parse_errors": len(errors),
        "verification": verification,
    }
    print(json.dumps(summary, indent=2))


